package week1day1;

public class WhileLoopDemo {

	public static void main(String[] args) {
		
		// Print all the numbers up to 10.
		int x = 0;
		
		while(x <= 10) { 
			// These statements will always repeat
			// as long as the condition is true.
			System.out.println(x);
			x++;
		}

	}

}
