package week1day1;

public class ForLoopDemo {
	
	public static void main(String[] args) {
		
		// Print all the numbers up to 10.
		// Use a for loop.
		
		// Declare a loop index i, and make go from 0
		// to 10.
		for (int i = 0; i <= 10; i++) {
			System.out.println(i);
		}
		
	}

}
